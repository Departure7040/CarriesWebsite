#!/usr/bin/env python3
"""Regenerate the review-card sections of site/testimonials.html from data/reviews.yaml,
and inject individual schema.org "Review" JSON-LD for rich snippets.

WHAT IT DOES
------------
1. Reads data/reviews.yaml (the single source of truth — itself derived VERBATIM from the
   page by build/reviews/_extract_reviews.py).
2. Rebuilds the two review-card grids (#google-reviews + #testimonials) and writes them back
   into site/testimonials.html BETWEEN sentinels:
       <!-- REVIEWS:AUTO:START -->  …generated…  <!-- REVIEWS:AUTO:END -->
   On the FIRST run the sentinels don't exist yet, so the builder finds the two existing
   sections (by their exact opening tag / the following trust-strip) and wraps+replaces them.
   On every later run it locate-and-replaces the block between the sentinels. The rest of the
   page (head, hero, #ratings, trust-strip, #leave-a-review, #contact-cta, footer) is preserved.
3. Injects ONE <script type="application/ld+json" id="reviews-jsonld"> array of per-review
   schema.org Review objects (author, reviewBody, itemReviewed -> the RealEstateAgent) IN
   ADDITION to the existing AggregateRating in <head> (which is left untouched). A reviewRating
   is emitted ONLY for reviews whose stored rating is non-null (the 3 visibly-starred Google
   cards) — never fabricated for the unrated testimonials.

Idempotent + re-runnable:

    python build/reviews/build_reviews.py

COMPLIANCE: the regenerated cards reproduce each review verbatim (byte-identical to the current
page on first run); no review, author, quote, or rating is invented. The page keeps its
<meta robots noindex>, demo banner, and the existing production caveat comment. A review-snippet
caveat comment is written inside the AUTO region flagging that individual Review markup must be
validated against Google's review-snippet rules before any indexable launch.
"""

import json
from pathlib import Path

import yaml

REPO = Path(__file__).resolve().parent.parent.parent
DATA = REPO / "data" / "reviews.yaml"
PAGE = REPO / "site" / "testimonials.html"

START = "<!-- REVIEWS:AUTO:START -->"
END = "<!-- REVIEWS:AUTO:END -->"

# First-run anchors (used only before the sentinels exist).
FIRST_RUN_START = '<section class="section section-alt" id="google-reviews">'
FIRST_RUN_END = '<div class="trust-strip">'

AGENT_NAME = "Carrie Billeaud, Realtor"

CAVEAT = """\
<!-- This region is GENERATED by build/reviews/build_reviews.py from data/reviews.yaml.
     Do not hand-edit between the AUTO:START / AUTO:END sentinels — rerun the builder instead.
     Every review below is reproduced VERBATIM from Carrie's website / public review profiles.
     REVIEW-SNIPPET CAVEAT: the individual Review JSON-LD block below is safe on this private
     noindex demo, but before any indexable launch it must be validated against Google's
     review-snippet rules (markup must match visible, clearly-sourced review content). A
     reviewRating is emitted ONLY for reviews that show a visible star rating on the page (the
     Google cards); unrated testimonials carry no rating in markup — nothing is fabricated. -->"""


def source_byline(source: str, rating) -> str:
    """The exact byline suffix after </strong>, matching the current page markup."""
    if source == "google":
        stars = "★" * int(rating)  # ★
        return f" · {stars} Google review"
    if source == "alignable":
        return " · via Alignable Recommendations"
    if source == "nextdoor":
        return " · via NextDoor"
    if source == "zillow":
        return " · via Zillow"
    return ""  # website testimonials show name only


def card_html(review: dict) -> str:
    text = review["text"]
    author = review["author"]
    suffix = source_byline(review["source"], review.get("rating"))
    return (
        '      <div class="card quote-card">\n'
        f'        <p class="quote">"{text}"</p>\n'
        f"        <p><strong>{author}</strong>{suffix}</p>\n"
        "      </div>"
    )


def build_jsonld(reviews: list) -> str:
    nodes = []
    for r in reviews:
        node = {
            "@context": "https://schema.org",
            "@type": "Review",
            "author": {"@type": "Person", "name": r["author"]},
            "reviewBody": r["text"],
            "itemReviewed": {"@type": "RealEstateAgent", "name": AGENT_NAME},
        }
        if r.get("rating") is not None:
            node["reviewRating"] = {
                "@type": "Rating",
                "ratingValue": str(int(r["rating"])),
                "bestRating": "5",
            }
        nodes.append(node)
    return json.dumps(nodes, indent=2, ensure_ascii=False)


def build_region(reviews: list) -> str:
    google = [r for r in reviews if r["source"] == "google"]
    others = [r for r in reviews if r["source"] != "google"]

    google_cards = "\n".join(card_html(r) for r in google)
    other_cards = "\n".join(card_html(r) for r in others)
    jsonld = build_jsonld(reviews)

    return (
        f"{START}\n"
        f"{CAVEAT}\n"
        '<section class="section section-alt" id="google-reviews">\n'
        '  <div class="wrap">\n'
        '    <p class="kicker">From Google</p>\n'
        "    <h2>Recent Google reviews</h2>\n"
        '    <div class="card-grid">\n'
        f"{google_cards}\n"
        "    </div>\n"
        "  </div>\n"
        "</section>\n"
        "\n"
        '<section class="section" id="testimonials">\n'
        '  <div class="wrap">\n'
        '    <p class="kicker">Client Testimonials</p>\n'
        "    <h2>From Carrie's website and public profiles</h2>\n"
        '    <div class="card-grid" style="grid-template-columns:1fr;max-width:860px">\n'
        f"{other_cards}\n"
        "    </div>\n"
        '    <p style="margin-top:1.25rem"><em>Testimonials from Carrie\'s existing website and public profiles; a production launch would confirm permissions and link each to its source.</em></p>\n'
        "  </div>\n"
        "</section>\n"
        "\n"
        '<script type="application/ld+json" id="reviews-jsonld">\n'
        f"{jsonld}\n"
        "</script>\n"
        f"{END}"
    )


def splice(html: str, region: str) -> str:
    if START in html and END in html:
        pre = html[: html.index(START)]
        post = html[html.index(END) + len(END) :]
        return pre + region + post
    # First run: wrap the two existing sections.
    if FIRST_RUN_START not in html or FIRST_RUN_END not in html:
        raise SystemExit(
            "Could not locate the review sections to wrap. Expected either the AUTO sentinels "
            f"or both anchors {FIRST_RUN_START!r} and {FIRST_RUN_END!r} in {PAGE}."
        )
    start_idx = html.index(FIRST_RUN_START)
    trust_idx = html.index(FIRST_RUN_END)
    if trust_idx < start_idx:
        raise SystemExit("Anchor order unexpected; aborting rather than guessing.")
    # Replace the two sections (and the trailing blank line before trust-strip) with the
    # sentinel-wrapped region, preserving the exact "\n\n" separation to trust-strip.
    return html[:start_idx] + region + "\n\n" + html[trust_idx:]


def main() -> None:
    data = yaml.safe_load(DATA.read_text(encoding="utf-8"))
    reviews = data["reviews"]

    html = PAGE.read_text(encoding="utf-8")
    region = build_region(reviews)
    new_html = splice(html, region)

    changed = new_html != html
    PAGE.write_text(new_html, encoding="utf-8")

    from collections import Counter

    by_src = Counter(r["source"] for r in reviews)
    rated = sum(1 for r in reviews if r.get("rating") is not None)
    print(f"{'Updated' if changed else 'No change to'} {PAGE}")
    print(f"  reviews: {len(reviews)}  by source: {dict(by_src)}")
    print(f"  JSON-LD Review nodes: {len(reviews)}  (with reviewRating: {rated})")
    print("  region written between REVIEWS:AUTO:START / REVIEWS:AUTO:END")


if __name__ == "__main__":
    main()
